#Практическое задание по уроку "Строки и индексация строк"

example = 'Nikita'

#Первый символ строки

print(example[0])

#Последний символ строки

print(example[-1])

#Вторую половину строки

print(example[3: ])

#Слово наоборот

print(example[ : :-1])

#Каждый второй символ строки

print(example[1:6:2])
